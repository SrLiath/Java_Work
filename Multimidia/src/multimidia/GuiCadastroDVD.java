    private void jB_CadastrarDVDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_CadastrarDVDActionPerformed
        // TODO add your handling code here:
        DVD novoDVD;
        novoDVD = new DVD(jTF_Titulo.getText(),
                jTF_Diretor.getText(),
                Integer.parseInt(jTF_Duracao.getText()));
        
        novoDVD.setDescricao(jtA_Descricao.getText());
        
        dataBase.setDvds(novoDVD);
        limpaTela();
    }//GEN-LAST:event_jB_CadastrarDVDActionPerformed
        
    private void limpaTela(){
        jTF_Titulo.setText(null);
        jTF_Diretor.setText(null);
        jTF_Duracao.setText(null);
        jtA_Descricao.setText(null);
        jCB_Tenho.setSelectedIndex(0);
        jTF_Titulo.requestFocus();
    }
    
